def _emit_class_header(ctx: _EmitCtx, outdir: str) -> str:
    cls = ctx.cls
    module = ctx.module
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#pragma once\n\n")
    out.write("#include \"oz_dispatch.h\"\n")
    if cls.superclass and cls.superclass in module.classes:
        out.write(f"#include \"{cls.superclass}.h\"\n")
    out.write("\n")

    # Struct
    _root_builtins = {"_refcount", "oz_class_id"}
    is_root = not cls.superclass or cls.superclass not in module.classes
    out.write(f"struct {cls.name} {{\n")
    if not is_root:
        out.write(f"\tstruct {cls.superclass} base;\n")
    else:
        out.write("\tuint8_t oz_class_id;\n")
        out.write("\tuint32_t _refcount;\n")
    for ivar in cls.ivars:
        if is_root and ivar.name in _root_builtins:
            continue
        out.write(f"\t{ivar.oz_type.c_type} {ivar.name};\n")
    out.write("};\n\n")

    # Root class always gets retain/release/retainCount prototypes
    if is_root:
        out.write(f"struct {cls.name} *{cls.name}_retain(struct {cls.name} *self);\n")
        out.write(f"void {cls.name}_release(struct {cls.name} *self);\n")
        out.write(f"uint32_t {cls.name}_retainCount(struct {cls.name} *self);\n")

    # Method prototypes
    has_dealloc = False
    for m in cls.methods:
        if is_root and m.selector in ("retain", "release", "retainCount"):
            continue
        if m.selector == "dealloc":
            has_dealloc = True
        out.write(f"{_method_prototype(cls, m)};\n")

    # Add dealloc prototype if it will be auto-generated
    if not has_dealloc:
        obj_ivars = [iv for iv in cls.ivars if iv.oz_type.is_object]
        if obj_ivars or not is_root:
            out.write(f"void {cls.name}_dealloc(struct {cls.name} *self);\n")

    if cls.methods or is_root:
        out.write("\n")

    path = os.path.join(outdir, f"{cls.name}.h")
    _write_file(path, out.getvalue())
    return path


