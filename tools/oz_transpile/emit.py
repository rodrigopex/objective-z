def _emit_functions(module: OZModule, outdir: str, root_class: str) -> str:
    """Generate oz_functions.c with top-level C functions from the ObjC source."""
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#include <zephyr/kernel.h>\n")
    out.write("#include <zephyr/sys/printk.h>\n")
    out.write("#include \"oz_dispatch.h\"\n")
    out.write("#include \"oz_mem_slabs.h\"\n")
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"#include \"{cls.name}.h\"\n")
    out.write("\n")

    # Use a dummy OZClass for body emission context (free functions use OZObject)
    dummy_cls = module.classes.get(root_class, OZClass(name=root_class))
    ctx = _EmitCtx(cls=dummy_cls, module=module, root_class=root_class)

    for func in module.functions:
        ret = func.return_type.c_type
        params_str = ", ".join(
            f"{p.oz_type.c_type} {p.name}" for p in func.params
        )
        if not params_str:
            params_str = "void"
        out.write(f"{ret} {func.name}({params_str})\n")
        ctx.method = None
        ctx.scope_vars = []
        ctx.consumed_vars = set()
        ctx.loop_scope_depth = []
        if func.body_ast:
            _emit_compound_stmt(func.body_ast, out, ctx, indent=0,
                                param_retains=_object_params(func))
        else:
            out.write("{\n}\n")
        out.write("\n")

    path = os.path.join(outdir, "oz_functions.c")
    _write_file(path, out.getvalue())
    return path


