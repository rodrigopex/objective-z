# oz_dispatch.h
# ---------------------------------------------------------------------------

def _emit_dispatch_header(module: OZModule, outdir: str) -> str:
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#pragma once\n\n")
    out.write("#include <stdint.h>\n")
    out.write("#include <stdbool.h>\n\n")
    out.write("#ifndef BOOL\n")
    out.write("#define BOOL _Bool\n")
    out.write("#endif\n\n")

    # Forward declare all class structs
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"struct {cls.name};\n")
    out.write("\n")

    # Class ID enum
    out.write("enum oz_class_id {\n")
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"\tOZ_CLASS_{cls.name} = {cls.class_id},\n")
    out.write(f"\tOZ_CLASS_COUNT = {len(module.classes)}\n")
    out.write("};\n\n")

    # Protocol dispatch macros
    _emit_protocol_macros(module, out)

    path = os.path.join(outdir, "oz_dispatch.h")
    _write_file(path, out.getvalue())
    return path


def _emit_protocol_macros(module: OZModule, out: StringIO) -> None:
    """Generate OZ_SEND macros for protocol-dispatched selectors."""
    # Collect all protocol-dispatched selectors with their signatures
    proto_sels: dict[str, OZMethod] = {}
    for cls in module.classes.values():
        for m in cls.methods:
            if m.dispatch == DispatchKind.PROTOCOL and m.selector not in proto_sels:
                proto_sels[m.selector] = m

    if not proto_sels:
        return

    out.write("/* Protocol dispatch function pointer types */\n")
    for sel, m in sorted(proto_sels.items()):
        c_sel = _selector_to_c(sel)
        ret = m.return_type.c_type
        param_types = ", ".join(
            ["struct OZObject *"] + [p.oz_type.c_type for p in m.params]
        )
        out.write(f"typedef {ret} (*OZ_fn_{c_sel})({param_types});\n")

    out.write("\n/* Protocol dispatch tables (indexed by oz_class_id) */\n")
    for sel, m in sorted(proto_sels.items()):
        c_sel = _selector_to_c(sel)
        out.write(f"extern OZ_fn_{c_sel} OZ_vtable_{c_sel}[OZ_CLASS_COUNT];\n")

    out.write("\n/* OZ_SEND macros (statement-expr to avoid double evaluation) */\n")
    for sel, m in sorted(proto_sels.items()):
        c_sel = _selector_to_c(sel)
        extra_params = [p.name for p in m.params]
        extra_args = ", ".join([f"({p.name})" for p in m.params])
        macro_params = ", ".join(["obj"] + extra_params)
        call = f"OZ_vtable_{c_sel}[_oz_obj->oz_class_id]"
        if extra_args:
            call += f"(_oz_obj, {extra_args})"
        else:
            call += "(_oz_obj)"
        out.write(
            f"#define OZ_SEND_{c_sel}({macro_params}) "
            f"__extension__({{ struct OZObject *_oz_obj = "
            f"(struct OZObject *)(obj); {call}; }})\n"
        )

    out.write("\n/* OZ_PROTOCOL_SEND (cast-free, same as OZ_SEND) */\n")
    for sel, m in sorted(proto_sels.items()):
        c_sel = _selector_to_c(sel)
        macro_params = ", ".join(["obj"] + [p.name for p in m.params])
        out.write(f"#define OZ_PROTOCOL_SEND_{c_sel}({macro_params}) "
                  f"OZ_SEND_{c_sel}({macro_params})\n")

    out.write("\n")


def _emit_dispatch_source(module: OZModule, outdir: str) -> str:
    """Generate oz_dispatch.c with vtable definitions and registration."""
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#include \"oz_dispatch.h\"\n")

    # Include all class headers (needed for function pointer casts)
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"#include \"{cls.name}.h\"\n")
    out.write("\n")

    # Collect all protocol-dispatched selectors
    emitted: set[str] = set()
    for cls in module.classes.values():
        for m in cls.methods:
            if m.dispatch == DispatchKind.PROTOCOL and m.selector not in emitted:
                c_sel = _selector_to_c(m.selector)
                out.write(f"OZ_fn_{c_sel} OZ_vtable_{c_sel}[OZ_CLASS_COUNT];\n")
                emitted.add(m.selector)

    # Generate oz_register_vtables()
    _emit_vtable_registration(module, out)

    path = os.path.join(outdir, "oz_dispatch.c")
    _write_file(path, out.getvalue())
    return path


def _emit_vtable_registration(module: OZModule, out: StringIO) -> None:
    """Generate oz_register_vtables() that populates all protocol vtable entries."""
    # Collect protocol entries: (selector, class, method) triples
    entries: list[tuple[str, OZClass, OZMethod]] = []
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        for m in cls.methods:
            if m.dispatch == DispatchKind.PROTOCOL:
                entries.append((_selector_to_c(m.selector), cls, m))

    if not entries:
        return

    out.write("\n__attribute__((constructor))\n")
    out.write("static void oz_register_vtables(void)\n")
    out.write("{\n")

    # For each protocol selector, walk the class hierarchy to fill vtable slots.
    # Each class gets the most specific implementation.
    proto_sels: dict[str, OZMethod] = {}
    for cls in module.classes.values():
        for m in cls.methods:
            if m.dispatch == DispatchKind.PROTOCOL and m.selector not in proto_sels:
                proto_sels[m.selector] = m

    for sel_name in sorted(proto_sels.keys()):
        c_sel = _selector_to_c(sel_name)
        # For each class, find the implementing class (walk up hierarchy)
        for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
            impl_cls = _find_implementing_class(cls, sel_name, module)
            if impl_cls:
                out.write(f"\tOZ_vtable_{c_sel}[OZ_CLASS_{cls.name}] = "
                          f"(OZ_fn_{c_sel}){impl_cls.name}_{c_sel};\n")

    out.write("}\n")


def _find_implementing_class(cls: OZClass, selector: str,
                             module: OZModule) -> OZClass | None:
    """Walk up the class hierarchy to find which class implements the selector."""
    cur: OZClass | None = cls
    while cur:
        for m in cur.methods:
            if m.selector == selector:
                return cur
        if cur.superclass and cur.superclass in module.classes:
            cur = module.classes[cur.superclass]
        else:
            break
    return None


