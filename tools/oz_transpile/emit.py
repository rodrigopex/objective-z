def _emit_mem_slabs(module: OZModule, outdir: str,
                    pool_sizes: dict[str, int], root_class: str) -> list[str]:
    """Generate oz_mem_slabs.h (declarations + inline) and oz_mem_slabs.c (definitions)."""
    files = []

    # --- Header ---
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#pragma once\n\n")
    out.write("#include <zephyr/kernel.h>\n")
    out.write("#include <string.h>\n\n")

    # Include all class headers so struct types are complete
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"#include \"{cls.name}.h\"\n")
    out.write("\n")

    # Extern declarations for slabs
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"extern struct k_mem_slab oz_slab_{cls.name};\n")
    out.write("\n")

    # Inline alloc/free
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        base_chain = _base_chain(cls.name, module)
        out.write(f"static inline struct {cls.name} *{cls.name}_alloc(void)\n")
        out.write("{\n")
        out.write(f"\tstruct {cls.name} *obj;\n")
        out.write(f"\tif (k_mem_slab_alloc(&oz_slab_{cls.name}, "
                  f"(void **)&obj, K_NO_WAIT) != 0) {{\n")
        out.write(f"\t\treturn (struct {cls.name} *)0;\n")
        out.write("\t}\n")
        out.write(f"\tmemset(obj, 0, sizeof(struct {cls.name}));\n")
        out.write(f"\t{base_chain}oz_class_id = OZ_CLASS_{cls.name};\n")
        out.write(f"\t{base_chain}_refcount = 1;\n")
        out.write("\treturn obj;\n")
        out.write("}\n\n")

        out.write(f"static inline void {cls.name}_free(struct {cls.name} *obj)\n")
        out.write("{\n")
        out.write(f"\tk_mem_slab_free(&oz_slab_{cls.name}, (void *)obj);\n")
        out.write("}\n\n")

    # Dispatch free by oz_class_id (root dealloc calls this)
    out.write(f"static inline void {root_class}_dispatch_free(struct {root_class} *obj)\n")
    out.write("{\n")
    out.write("\tswitch (obj->oz_class_id) {\n")
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        out.write(f"\tcase OZ_CLASS_{cls.name}: "
                  f"{cls.name}_free((struct {cls.name} *)obj); break;\n")
    out.write("\t}\n")
    out.write("}\n\n")

    h_path = os.path.join(outdir, "oz_mem_slabs.h")
    _write_file(h_path, out.getvalue())
    files.append(h_path)

    # --- Source ---
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write("#include \"oz_mem_slabs.h\"\n\n")

    auto_counts = _count_alloc_calls(module)
    for cls in sorted(module.classes.values(), key=lambda c: c.class_id):
        count = pool_sizes.get(cls.name, max(auto_counts.get(cls.name, 0), 1))
        out.write(f"K_MEM_SLAB_DEFINE(oz_slab_{cls.name}, "
                  f"sizeof(struct {cls.name}), {count}, 4);\n")

    c_path = os.path.join(outdir, "oz_mem_slabs.c")
    _write_file(c_path, out.getvalue())
    files.append(c_path)

    return files


