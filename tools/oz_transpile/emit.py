# Per-class .c
# ---------------------------------------------------------------------------

def _emit_class_source(ctx: _EmitCtx, outdir: str) -> str:
    cls = ctx.cls
    module = ctx.module
    root_class = ctx.root_class
    out = StringIO()
    out.write("/* Auto-generated by oz_transpile -- do not edit */\n")
    out.write(f"#include \"{cls.name}.h\"\n")
    out.write("#include \"oz_mem_slabs.h\"\n")
    if cls.name == root_class:
        out.write("#include <zephyr/sys/atomic.h>\n")
    out.write("\n")

    # Emit retain/release for root class
    if cls.name == root_class:
        _emit_root_retain_release(cls, module, out)

    has_user_dealloc = False
    for m in cls.methods:
        if m.selector in ("retain", "release", "retainCount") and cls.name == root_class:
            continue
        if m.selector == "dealloc":
            has_user_dealloc = True
            _emit_user_dealloc(ctx, m, out)
            continue
        ctx.method = m
        ctx.scope_vars = []
        ctx.consumed_vars = set()
        ctx.loop_scope_depth = []
        out.write(f"{_method_prototype(cls, m)}\n")
        if m.body_ast:
            _emit_compound_stmt(m.body_ast, out, ctx, indent=0,
                                param_retains=_object_params(m))
        else:
            out.write("{\n}\n")
        out.write("\n")

    # Auto-generate dealloc if not user-defined
    if not has_user_dealloc:
        _emit_auto_dealloc(ctx, out)

    path = os.path.join(outdir, f"{cls.name}.c")
    _write_file(path, out.getvalue())
    return path


# ---------------------------------------------------------------------------
# Root class retain/release
# ---------------------------------------------------------------------------

def _emit_root_retain_release(cls: OZClass, module: OZModule,
                              out: StringIO) -> None:
    out.write(f"struct {cls.name} *{cls.name}_retain(struct {cls.name} *self)\n")
    out.write("{\n")
    out.write("\tif (self) {\n")
    out.write(f"\t\tatomic_inc((atomic_t *)&self->_refcount);\n")
    out.write("\t}\n")
    out.write("\treturn self;\n")
    out.write("}\n\n")

    out.write(f"void {cls.name}_release(struct {cls.name} *self)\n")
    out.write("{\n")
    out.write("\tif (!self) {\n")
    out.write("\t\treturn;\n")
    out.write("\t}\n")
    out.write(f"\tatomic_val_t old = atomic_dec((atomic_t *)&self->_refcount);\n")
    out.write("\tif (old == 1) {\n")
    out.write(f"\t\tOZ_SEND_dealloc((struct {cls.name} *)self);\n")
    out.write("\t}\n")
    out.write("}\n\n")

    out.write(f"uint32_t {cls.name}_retainCount(struct {cls.name} *self)\n")
    out.write("{\n")
    out.write("\tif (!self) {\n")
    out.write("\t\treturn 0;\n")
    out.write("\t}\n")
    out.write(f"\treturn (uint32_t)atomic_get((atomic_t *)&self->_refcount);\n")
    out.write("}\n\n")


